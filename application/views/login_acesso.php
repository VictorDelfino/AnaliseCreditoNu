	function mCNPJ(cnpj){
		/* cnpj=cnpj.replace(/\D/g,"")
		cnpj=cnpj.replace(/^(\d{2})(\d)/,"$1.$2")
		cnpj=cnpj.replace(/^(\d{2})\.(\d{3})(\d)/,"$1.$2.$3")
		cnpj=cnpj.replace(/\.(\d{3})(\d)/,".$1/$2")
        cnpj=cnpj.replace(/(\d{4})(\d)/,"$1-$2").substring(0,18) */

        cnpj=cnpj.replace(/\W/g,"")
		cnpj=cnpj.replace(/^(\w{2})(\w)/,"$1.$2")
		cnpj=cnpj.replace(/^(\w{2})\.(\w{3})(\w)/,"$1.$2.$3")
		cnpj=cnpj.replace(/\.(\w{3})(\w)/,".$1/$2")
        cnpj=cnpj.replace(/(\w{4})(\w)/,"$1-$2").substring(0,18)

		return cnpj
	}
	function mCPF(cpf){
		cpf=cpf.replace(/\W/g,"")
		cpf=cpf.replace(/(\w{3})(\w)/,"$1.$2")
		cpf=cpf.replace(/(\w{3})(\w)/,"$1.$2")
		cpf=cpf.replace(/(\w{3})(\w{1,2})$/,"$1-$2").substring(0,14)
		return cpf
	}

